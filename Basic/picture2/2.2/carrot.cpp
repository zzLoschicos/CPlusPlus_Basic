// carrots.cpp -- food processing program
// uses and displays a variable
#include <iostream>

int main()
{
    using namespace std;

    int carrots; // 声明一个整数变量

    carrots = 25; // 给变量carrots赋值为25
    cout << "I have ";
    cout << carrots;  // 显示变量的值
    cout << " carrots.";
    cout << endl;
    carrots = carrots - 1; // 修改变量
    cout << "Crunch, crunch. Now I hava " << carrots << " carrots." << endl; 








    return 0;
}
